        <h2 class="excerpt">Introduction</h2>
        <p><em>Shangri-La?</em> Odd name for a diet. Name of a spa, maybe. I chose it partly because Shangri-La, James Hilton's fictional Himalayan community, was a place of great peace and tranquility; and this diet puts people at peace with food. Within days after starting it, all sorts of food-related struggles (irresistible cravings, too many food-related thoughts, uncontrollable night eating) usually go away. Another reason for the name was that Shangri-La was meant to be a near-perfect place and &#x2014; not to boast &#x2014; this is a diet with many advantages. It is simple, powerful, and does not require deprivation. It's almost as easy as taking a pill, and a hundred times safer and less expensive. I also liked the idea of naming a diet after an imaginary place. Imaginary places are often full of novelty and hope &#x2014; think Alice in Wonderland or Harry Potter. No harm in borrowing some of that.</p>
        <p>Writing this book was easier than I had expected, partly because of blog postings about the diet that appeared while I was writing it. Many of them expressed pleasure and amazement. "Ridiculously easy. And cheap. And effective. What hath God wrought?" wrote one person trying the diet. "I can hardly believe it myself," wrote someone else in response to skepticism &#x2014; someone for whom the diet was working. It was a little like being able to see the future: A glimpse of how the diet would be received.</p>
        <p>The funny thing is that I too have some trouble believing it &#x2014; and I discovered it. Five years ago. Not long enough for my could-this-really-be? reaction to entirely fade away. Unlike everyone else who has this reaction, however, I can explain how such a strange diet came to be. My secret weapon, as it were, is that I combined three methods of investigation that had not been combined before. One was my scientific training, which made it easy for me to understand the relevant research literature. Another method I used was self-experimentation. I myself wanted to lose weight and tried many ways of doing so. The third method was to play reporter: To phone weight-control experts and ask them to explain their research. (I learned to do this when I wrote for <em>Spy</em> magazine.) By themselves, none of these methods is unusual, but they are rarely if ever combined. For example, millions of people try various ways of losing weight, but few obesity researchers do so (or at least don't mention it) &#x2014; self-experimentation is considered disreputable. Because I studied weight loss in a new way, it is more understandable that I reached surprising conclusions.</p>
        <p>After a talk by a young scientist, during the question period, Niels Bohr, the Danish physicist, said to him, "We are all agreed that your theory is crazy. The question that divides us is whether it is crazy enough to have a chance of being correct." That was a rather flashy way of putting it. In an essay in Atomic <em>Physics and Human Knowledge</em>, Bohr was more sober: "The common aim of all science," he wrote, is "the gradual removal of prejudices." It was another way of saying the same thing: The truth (that is, good science) may appear crazy at first, because it does not fit our accepted views of how things work.</p>
        <p>Everyone will agree that the Shangri-La diet contradicts accepted views about how to lose weight. In the 1980s, the mantra was <em>eat less fat</em>. Consumers were offered low-fat pizza, low-fat cookies, low-fat everything. The Shangri-La diet says you can lose weight by consuming <em>more fat</em> (in the form of flavorless oils). In the 1990s, the mantra became <em>eat fewer carbs</em>. The Shangri-La diet says you can lose weight by consuming <em>more</em> sugar, the worst carb of all. If Niels Bohr wanted to lose weight, I like to think he would try it.</p>
