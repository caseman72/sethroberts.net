        <p>
          <a href="http://www.pubmedcentral.nih.gov/picrender.fcgi?artid=1338316&amp;blobtype=pdf">Quasi-reinforcement</a> is a way of making reward more powerful. It was discovered by Allen Neuringer and Shin-Ho Chung in pigeon experiments in the 1960s. Suppose a pigeon is given food every 500 times it pecks a key. It will peck a key for such "lean" reward, but slowly. Now suppose the experimenter briefly turns on a light every 20 pecks. The pigeon will roughly double its rate of pecking. The salary hasn't changed &#x2014; the light alone has no value to the pigeon, it will not peck to turn on the light &#x2014; but output has doubled. What an incredible effect! It has not been studied much but has been found with rats.  <img src="/images/whileeuropeBook.jpg" alt="While Europe Slept Book" width="73" height="110" class="bookFloatLeft"/>
        </p>
