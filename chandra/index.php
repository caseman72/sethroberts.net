        <h1>The Strange Case of Dr. Ranjit Chandra </h1>
        <p class="quoteTop">
          "I don't think one can conclude that everything Dr. Chandra<br/>
          did is under suspicion." &#x2014; <em><a href="http://www.themuse.ca/view.php?aid=38529">Axel Meisen</a>, President of</em><br/>
          <em>Memorial University of Newfoundland</em>
        </p>
        <p>Dr. Ranjit Chandra was a world-famous research scientist at the Memorial University of Newfoundland. His specialty was nutritional immunology. In 1990, he was named an Officer of the Order of Canada, Canada's highest honor. But all was not what it seemed ...</p>
