        <h1><em>Spy</em> magazine</h1>
        <p>Not just "Separated at Birth", but also Elvis Presley's weight on other planets, "Log-rolling in our time" (reciprocal book blurbs), and "The <em>Spy</em> 100" ("our annual list of the 100 most annoying, alarming and appalling people, places and things"). A <em>Spy</em> 100 entry from 1990: "83. The Democracy Hook. Leave it to corporate America to single out a joyous world event - the dismantling of the Berlin Wall - and sully it. Scarcely had the Eastern Europeans begun to revel when Pepsico, AT&amp;T and Jovan arrived in Berlin to film gloating, we-capitalists-were-right-all-along ads for the folks back home." In its heyday, I read all of every issue three times. It was more than enjoyable; it was analgesic. I read it at the gym while working out - nothing made exercise more tolerable than <em>Spy</em>. </p>

